// Глобальный setup для тестов.
// Проблема: модульные/интеграционные тесты поднимают и гасят множество HTTP-серверов на
// эфемерных портах. Глобальный пул keep-alive соединений undici (его использует fetch) мог
// переиспользовать сокет к порту, который уже занял ДРУГОЙ сервер, — и запрос уходил «не туда»
// (флейки вида «expected 404 to be 401»). Отключаем удержание соединений: сокет закрывается
// сразу после ответа, переиспользования между серверами нет.
try {
  const { setGlobalDispatcher, Agent } = await import('undici');
  setGlobalDispatcher(new Agent({ keepAliveTimeout: 1, keepAliveMaxTimeout: 1, pipelining: 0 }));
} catch {
  // undici — опциональная зависимость; если её нет, просто пропускаем (тесты не падают).
}
